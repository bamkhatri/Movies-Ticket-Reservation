package com.cite.moviesticketreservation.model;

public class SessionToken {

	public String token;

	public SessionToken() {
	}

	public SessionToken(String token) {
		this.token = token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
}
